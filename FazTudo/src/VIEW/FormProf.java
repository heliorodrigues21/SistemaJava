    // Método para preencher Tabela
    private void preencherTabela() {
        DefaultTableModel modelo = (DefaultTableModel) tabelaProf.getModel();
        modelo.setNumRows(0);

        ProfessorDao listProf = new ProfessorDao();
        for (Professor p : listProf.listarProfessor()) {
            modelo.addRow(new Object[]{
                p.getCodigoProf(),
                p.getNome(),
                p.getEndereco(),
                p.getCodigoCidade(),
                p.getTelefone(),
                p.getFormacao()

            }
            );
        }
    }

