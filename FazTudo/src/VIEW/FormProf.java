    //Método salvar 
    public boolean SalvarProfessor() {

        Professor professor = new Professor();
        professor.setNome(jNome.getText());
        professor.setEndereco(jEnd.getText());
        professor.setCodigoCidade(cidades.get(combCid.getSelectedIndex()));
        professor.setTelefone(jTel.getText());
        professor.setFormacao(jForm.getText());
        ProfessorDao profDao = new ProfessorDao();
        profDao.SalvarProfessor(professor);

        return true;

    }

    // Método Atualizar 
    public boolean AtualizarProfessor() {

        Professor professor = new Professor();

        professor.setCodigoProf(Integer.parseInt(jcod.getText()));
        professor.setNome(jNome.getText());
        professor.setEndereco(jEnd.getText());
        professor.setCodigoCidade(cidades.get(combCid.getSelectedIndex()));
        professor.setTelefone(jTel.getText());
        professor.setFormacao(jForm.getText());
        ProfessorDao profDao = new ProfessorDao();
        profDao.AtualizarProfessor(professor);

        return true;

    }
    
    // Método de excluir os dados de acordo com código selecionado

    private boolean ExcluirProfessor() {
        // Classe que contém os métodos get e set
        Professor professor = new Professor();
        professor.setCodigoProf(Integer.parseInt(jcod.getText()));
        // Classe que contém o método excluir 
        ProfessorDao dao = new ProfessorDao();
        dao.Excluir(professor);

        return true;

    }

    //Método Carregar Tabela
    public void CarregarTabela() {

        Vector cabecalho;
        cabecalho = new Vector();
        cabecalho.add("Código");
        cabecalho.add("Nome");
        cabecalho.add("Endereço");
        cabecalho.add("Cidade");
        cabecalho.add("Telefone");
        cabecalho.add("Formação");

        ProfessorDao profDao = new ProfessorDao();
        tabelaProf.setModel(new DefaultTableModel(profDao.CarregarProfTabela(), cabecalho));
    }
    
    
   
